// kono akta array er sob upadan ke bar bar calanor jonno amra for loop or while loop use korte pariii 
// Example

// for loop er jonno
let fruite = ['Apple', 'Banana', 'Orange', 'Jackfruites', 'Mango', 'Watermelon'];

for(let index = 0; index < fruite.length; index++){
    console.log(fruite[index])
}

// while loop er jonno 
let fol = 0;

while(fol < fruite.length){
    console.log(fruite[fol]);
    fol++
}
